        mask_x = weight_x < 0.01
        mask_y = weight_y < 0.01

        weight_x[mask_x] = 0.01
        weight_y[mask_y] = 0.01

        weight_x = torch.from_numpy(weight_x).cuda()
        weight_y = torch.from_numpy(weight_y).cuda()

        return weight_x, weight_y

