        illu_x = self.grandient_x(illu)
        illu_y = self.grandient_y(illu)

        weight_x, weight_y = self.compute_weight(img)

        smooth_x = torch.mean(torch.abs(illu_x / weight_x))
        smooth_y = torch.mean(torch.abs(illu_y / weight_y))

        return smooth_x + smooth_y


