class max_weighted_smooth_loss(nn.Module):
    def __init__(self):
        super(max_weighted_smooth_loss, self).__init__()

    def grandient_x(self, img):
        gx = img[:, :, :-1, :] - img[:, :, 1:, :]
        return gx

    def grandient_y(self, img):
        gy = img[:, :, :, :-1] - img[:, :, :, 1:]
        return gy

